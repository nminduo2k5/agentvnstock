    def render_stock_chart(self, stock_symbol: str):
        """Render stock price chart"""
        try:
            # Get historical data
            historical_data = asyncio.run(self.stock_api.get_historical_data(stock_symbol, days=30))
            
            if not historical_data:
                st.warning("‚ö†Ô∏è Kh√¥ng c√≥ d·ªØ li·ªáu l·ªãch s·ª≠")
                return
            
            # Create DataFrame
            df = pd.DataFrame(historical_data)
            df['date'] = pd.to_datetime(df['date'])
            
            # Create candlestick chart
            fig = go.Figure()
            
            # Price line
            fig.add_trace(go.Scatter(
                x=df['date'],
                y=df['price'],
                mode='lines',
                name='Price',
                line=dict(color='#1f77b4', width=2)
            ))
            
            # Volume bars
            fig.add_trace(go.Bar(
                x=df['date'],
                y=df['volume'],
                name='Volume',
                yaxis='y2',
                opacity=0.3,
                marker_color='gray'
            ))
            
            # Layout
            fig.update_layout(
                title=f"Bi·ªÉu ƒë·ªì gi√° {stock_symbol} - 30 ng√†y g·∫ßn nh·∫•t",
                xaxis_title="Th·ªùi gian",
                yaxis_title="Gi√° (VND)",
                yaxis2=dict(
                    title="Volume",
                    overlaying='y',
                    side='right'
                ),
                height=400,
                showlegend=True
            )
            
            st.plotly_chart(fig, use_container_width=True)
            
        except Exception as e:
            st.error(f"‚ùå L·ªói t·∫°o bi·ªÉu ƒë·ªì: {e}")
    
